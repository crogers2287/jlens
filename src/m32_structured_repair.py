def load_manifest(path):
    manifest = json.loads(Path(path).read_text())
    if manifest.get("selection_status") != "predeclared_before_m32_task_generation":
        raise ValueError("M32 manifest is not predeclared before generation")
    bands = manifest.get("bands") or []
    tasks = manifest.get("tasks") or []
    if manifest.get("n_tasks") != 384 or len(tasks) != 384 or len(bands) != 6:
        raise ValueError("M32 manifest must declare six bands and 384 tasks")
    if len({task["task_id"] for task in tasks}) != 384:
        raise ValueError("M32 task ids must be unique")
    counts = Counter(task["band"] for task in tasks)
    for band in bands:
        if band.get("n_tasks") != 64 or counts[band["band_id"]] != 64:
            raise ValueError(f"M32 band size mismatch: {band['band_id']}")
    if manifest["frozen_trigger"].get("threshold_fail_probability") != 0.5:
        raise ValueError("M32 frozen trigger threshold changed")
    order = manifest["repair_operators"].get("candidate_order_frozen")
    if tuple(order or ()) != STRUCTURED_ORDER:
        raise ValueError("M32 frozen candidate order changed")
    return manifest


def prior_tuples_all(m29_path, m30_path, m31_path):
    """Every operand tuple generated by the M29, M30, and M31 manifests."""
    prior = M31.prior_tuples_by_band(m29_path, m30_path)
    m31_manifest = M31.load_manifest(m31_path)
    seed = m31_manifest["generation"]["seed"]
    for band in m31_manifest["bands"]:
        rng = random.Random(f"{seed}:{band['band_id']}")
        seen = set(prior[band["band_id"]])
        tuples = M30._draw_tuples(rng, band, band["n_tasks"], seen)
        prior[band["band_id"]] |= set(tuples)
    return prior


def generate_tasks(manifest, m29_path, m30_path, m31_path):
    seed = manifest["generation"]["seed"]
    template = manifest["prompt_template"]
    prior = prior_tuples_all(m29_path, m30_path, m31_path)
    slots = defaultdict(list)
    for task in manifest["tasks"]:
        slots[task["band"]].append(task)
    tasks = []
    for band in manifest["bands"]:
        band_id = band["band_id"]
        rng = random.Random(f"{seed}:{band_id}")
        seen = set(prior[band_id])
        tuples = M30._draw_tuples(rng, band, band["n_tasks"], seen)
        for slot, (a, b) in zip(slots[band_id], tuples):
            tasks.append({
                "prompt_id": slot["task_id"],
                "m32_band": band_id,
                "task_category": "math",
                "a": a, "b": b,
                "expression": f"{a}*{b}",
                "known_answer": str(a * b),
                "prompt": template.format(a=a, op="*", b=b),
            })
    if len(tasks) != 384 or len({t["prompt_id"] for t in tasks}) != 384:
        raise ValueError("M32 generation did not produce 384 unique tasks")
    order = {task["task_id"]: index
             for index, task in enumerate(manifest["tasks"])}
    tasks.sort(key=lambda task: order[task["prompt_id"]])
    return tasks


def prepare_private_tasks(manifest_path, m29_path, m30_path, m31_path,
                          output_path):
    manifest = load_manifest(manifest_path)
    tasks = generate_tasks(manifest, m29_path, m30_path, m31_path)
    out = Path(output_path); out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text("".join(json.dumps(task) + "\n" for task in tasks))
    return manifest, tasks


def repair_prompts(manifest, task, original_answer, diagnosis=None):
    ops = manifest["repair_operators"]
    prompts = {
        "independent_deliberate": ops["independent_deliberate"][
            "prompt_template"].format(a=task["a"], b=task["b"]),
        "checker_guided_repair": ops["checker_guided_repair"][
            "prompt_template"].format(a=task["a"], b=task["b"],
                                      original_answer=original_answer),
        "diagnose_stage_a": ops["diagnose_then_repair"]["stage_a"][
            "prompt_template"].format(a=task["a"], b=task["b"],
                                      original_answer=original_answer),
    }
    if diagnosis is not None:
        prompts["diagnose_stage_b"] = ops["diagnose_then_repair"]["stage_b"][
            "prompt_template"].format(a=task["a"], b=task["b"],
                                      diagnosis=diagnosis)
    return prompts


def verdict_for(output, task):
    result = VZ.math_checker(output or "", known_answer=task["known_answer"],
                             expression=task["expression"])
    return result["verdict"] if result["verdict"] in ("pass", "fail") \
        else "undecided"


def run_captures(manifest, tasks, model_ref, captures_root):
    """Single-model-load staged capture driver for all M32 decode phases."""
    import capture_router_logits as CAP
    import torch

    args = argparse.Namespace(
        model=model_ref, dtype="bf16", device_map="auto",
        max_gpu_mem_gib=20.0, trust_remote_code=False)
    tok, model, cfg = CAP.load_model(args)
    root = Path(captures_root)

    def capture(prompt_id, text, out_dir, cap_tokens, temperature=None,
                seed=None):
        dest = root / out_dir / f"{prompt_id}.pt"
        if CAP._valid_capture(dest):
            return dest
        dest.parent.mkdir(parents=True, exist_ok=True)
        input_ids, router, hidden, steps = CAP.capture_one(
            tok, model, text, 4096, max_new_tokens=cap_tokens,
            router_only=True, chat_template=True, temperature=temperature,
            sample_seed=seed)
        payload = {"prompt_id": prompt_id, "input_ids": input_ids,
                   "router_logits": router, "hidden_states": hidden,
                   "model_type": cfg.model_type, "model_path": str(model_ref),
                   "decode_steps": steps,
                   "generated_output": tok.decode(
                       [s["generated_token_id"] for s in steps],
                       skip_special_tokens=True)}
        torch.save(payload, dest)
        return dest

    ops = manifest["repair_operators"]
    print(f"[jlens] M32 phase 1: originals ({len(tasks)})", flush=True)
    fails = []
    for task in tasks:
        dest = capture(task["prompt_id"], task["prompt"],
                       CAPTURE_DIRS["original"], 64)
        payload = torch.load(dest, map_location="cpu", weights_only=False)
        if verdict_for(payload.get("generated_output"), task) == "fail":
            fails.append(task)
    print(f"[jlens] M32 originals done; failing originals={len(fails)}",
          flush=True)

    print("[jlens] M32 phase 2: resample_t07 (all tasks)", flush=True)
    seed_prefix = ops["resample_t07"]["seed_prefix"]
    for task in tasks:
        capture(task["prompt_id"], task["prompt"],
                CAPTURE_DIRS["resample_t07"],
                ops["resample_t07"]["decode_cap_tokens"],
                temperature=ops["resample_t07"]["temperature"],
                seed=CAP._stable_seed(f"{seed_prefix}:{task['prompt_id']}"))

    original_answers = {}
    for task in fails:
        payload = torch.load(root / CAPTURE_DIRS["original"]
                             / f"{task['prompt_id']}.pt",
                             map_location="cpu", weights_only=False)
        original_answers[task["prompt_id"]] = VZ._final_number(
            payload.get("generated_output") or "") or "unknown"

    for phase, out_key, cap_key in (
            ("independent_deliberate", "independent_deliberate",
             ("independent_deliberate", "decode_cap_tokens")),
            ("checker_guided_repair", "checker_guided_repair",
             ("checker_guided_repair", "decode_cap_tokens")),
            ("diagnose_stage_a", "diagnose_stage_a",
             ("diagnose_then_repair", "stage_a", "decode_cap_tokens"))):
        cap_tokens = ops
        for key in cap_key:
            cap_tokens = cap_tokens[key]
        print(f"[jlens] M32 phase: {phase} ({len(fails)} failing tasks)",
              flush=True)
        for task in fails:
            prompts = repair_prompts(
                manifest, task, original_answers[task["prompt_id"]])
            capture(task["prompt_id"], prompts[phase],
                    CAPTURE_DIRS[out_key], cap_tokens)

    print(f"[jlens] M32 phase: diagnose_stage_b ({len(fails)})", flush=True)
    stage_b_cap = ops["diagnose_then_repair"]["stage_b"]["decode_cap_tokens"]
    for task in fails:
        stage_a = torch.load(root / CAPTURE_DIRS["diagnose_stage_a"]
                             / f"{task['prompt_id']}.pt",
                             map_location="cpu", weights_only=False)
        prompts = repair_prompts(
            manifest, task, original_answers[task["prompt_id"]],
            diagnosis=(stage_a.get("generated_output") or "").strip())
        capture(task["prompt_id"], prompts["diagnose_stage_b"],
                CAPTURE_DIRS["diagnose_then_repair"], stage_b_cap)
    print("[jlens] M32 captures complete", flush=True)


def _success_delta_bootstrap(success_a, success_b, seed, iterations=2000):
    rng = random.Random(seed)
    n = len(success_a)
    deltas = []
    for _ in range(iterations):
        indices = [rng.randrange(n) for _ in range(n)]
        deltas.append((sum(success_a[i] for i in indices)
                       - sum(success_b[i] for i in indices)) / n)
    deltas.sort()
    return [deltas[int(0.025 * (iterations - 1))],
            deltas[int(0.975 * (iterations - 1))]]


def _majority_answer(candidates, order):
    numbers = [(name, VZ._final_number(candidates[name]["output"] or ""))
               for name in order if name in candidates]
    numbers = [(name, value) for name, value in numbers if value is not None]
    if not numbers:
        return None
    counts = Counter(value for _, value in numbers)
    top = max(counts.values())
    tied = {value for value, count in counts.items() if count == top}
    for name, value in numbers:
        if value in tied:
            return value
    return None


def evaluate(manifest, rows):
    """rows: per-task dicts with original/candidate verdicts and telemetry."""
    threshold = manifest["frozen_trigger"]["threshold_fail_probability"]
    n = len(rows)
    triggered = {row["task_id"] for row in rows if row["p_fail"] >= threshold}
    fails = {row["task_id"] for row in rows if not row["original_pass"]}
    rng = random.Random("m32:random-trigger")
    random_set = set(rng.sample(sorted(row["task_id"] for row in rows),
                                len(triggered)))

    def bundle_passes(row):
        return next((name for name in STRUCTURED_ORDER
                     if row["candidates"].get(name, {}).get("pass")), None)

    attempted_by_policy = {
        "no_repair": set(),
        "always_structured_bundle": {row["task_id"] for row in rows},
        "random_trigger_structured_bundle": random_set,
        "telemetry_trigger_structured_bundle": triggered,
        "telemetry_trigger_resample_only": triggered,
        "telemetry_trigger_tool_upper_bound": triggered,
    }

    success, metrics = {}, {}
    for policy, attempted in attempted_by_policy.items():
        outcomes, tokens, candidates_used = [], 0, 0
        rescued = introduced = false_alarms = 0
        for row in rows:
            attack = row["task_id"] in attempted
            original_pass = row["original_pass"]
            final_pass = original_pass
            if attack:
                if policy == "telemetry_trigger_resample_only":
                    candidate = row["candidates"].get("resample_t07", {})
                    final_pass = bool(candidate.get("pass"))
                    tokens += candidate.get("tokens", 0)
                    candidates_used += 1
                    if original_pass and not final_pass:
                        introduced += 1
                    if original_pass:
                        false_alarms += 1
                elif policy == "telemetry_trigger_tool_upper_bound":
                    final_pass = True
                    if original_pass:
                        false_alarms += 1
                else:  # verifier-first structured bundle
                    if original_pass:
                        false_alarms += 1  # verifier check, no candidates used
                    else:
                        winner = bundle_passes(row)
                        for name in STRUCTURED_ORDER:
                            candidate = row["candidates"].get(name)
                            if candidate is None:
                                continue
                            tokens += candidate.get("tokens", 0)
                            candidates_used += 1
                            if name == winner:
                                break
                        if winner is not None:
                            final_pass = True
                if not original_pass and final_pass:
                    rescued += 1
                if original_pass and not final_pass:
                    pass  # counted above for resample arm only
            outcomes.append(1 if final_pass else 0)
        success[policy] = outcomes
        metrics[policy] = {
            "verified_success_rate": sum(outcomes) / n,
            "tasks_attempted": len(attempted),
            "candidates_consumed": candidates_used,
            "candidate_decode_tokens": tokens,
            "false_alarm_checks": false_alarms,
            "errors_rescued": rescued,
            "errors_introduced": introduced,
            "tokens_per_rescue": (tokens / rescued) if rescued else None,
        }
        # Stop-condition assertion: verifier-first arms never harm.
        if policy.endswith("structured_bundle") and metrics[policy][
                "errors_introduced"]:
            raise ValueError(
                "M32 stop: structured bundle replaced a passing original")

    trigger_hits = len(triggered & fails)
    h1_rows = [row for row in rows
               if row["task_id"] in triggered and not row["original_pass"]]
    h1_bundle = [1 if bundle_passes(row) else 0 for row in h1_rows]
    h1_resample = [1 if row["candidates"].get("resample_t07", {}).get("pass")
                   else 0 for row in h1_rows]
    h1_interval = _success_delta_bootstrap(
        h1_bundle, h1_resample,
        manifest["hypotheses"]["h1_repair_improvement"]["bootstrap_seed"])
    if h1_interval[0] > 0:
        h1_verdict = "repair_improved"
    elif h1_interval[1] < 0:
        h1_verdict = "repair_worse"
    else:
        h1_verdict = "repair_not_established"

    seed_prefix = manifest["hypotheses"]["h2_policy_usefulness"][
        "bootstrap_seed_prefix"]
    h2 = {}
    for name_a, name_b in manifest["hypotheses"]["h2_policy_usefulness"][
            "comparisons"]:
        interval = _success_delta_bootstrap(
            success[name_a], success[name_b],
            f"{seed_prefix}:{name_a}-vs-{name_b}")
        h2[f"{name_a}_vs_{name_b}"] = {
            "delta_success_rate": (
                metrics[name_a]["verified_success_rate"]
                - metrics[name_b]["verified_success_rate"]),
            "delta_success_bootstrap_95pct": interval,
        }
    over_none = h2["telemetry_trigger_structured_bundle_vs_no_repair"][
        "delta_success_bootstrap_95pct"]
    over_random = h2["telemetry_trigger_structured_bundle_vs_"
                     "random_trigger_structured_bundle"][
        "delta_success_bootstrap_95pct"]
    if over_none[0] > 0 and over_random[0] > 0:
        h2_verdict = "useful"
    elif over_none[1] < 0:
        h2_verdict = "harmful"
    else:
        h2_verdict = "not_established"

    fail_rows = [row for row in rows if not row["original_pass"]]
    operator_stats, rescue_sets = {}, {}
    for name in ALL_OPERATORS:
        available = [row for row in fail_rows if name in row["candidates"]]
        passing = {row["task_id"] for row in available
                   if row["candidates"][name].get("pass")}
        rescue_sets[name] = passing
        operator_stats[name] = {
            "candidates_generated": len(available),
            "candidate_pass_rate": (len(passing) / len(available)
                                    if available else None),
            "mean_decode_tokens": (statistics.fmean(
                row["candidates"][name].get("tokens", 0)
                for row in available) if available else None),
        }
    union_others = {}
    for name in ALL_OPERATORS:
        others = set().union(*(rescue_sets[o] for o in ALL_OPERATORS
                               if o != name)) if len(ALL_OPERATORS) > 1 else set()
        union_others[name] = len(rescue_sets[name] - others)

    rerank_rows = [row for row in fail_rows
                   if all(name in row["candidates"]
                          and row["candidates"][name].get("p_fail") is not None
                          for name in STRUCTURED_ORDER)]
    rerank_correct = rerank_when_possible = 0
    majority_pass = 0
    first_candidate_pass = 0
    for row in rerank_rows:
        ranked = sorted(
            STRUCTURED_ORDER,
            key=lambda name: (row["candidates"][name]["p_fail"],
                              STRUCTURED_ORDER.index(name)))
        selected = ranked[0]
        any_passing = any(row["candidates"][name].get("pass")
                          for name in STRUCTURED_ORDER)
        if any_passing:
            rerank_when_possible += 1
            if row["candidates"][selected].get("pass"):
                rerank_correct += 1
        first_candidate_pass += bool(
            row["candidates"][STRUCTURED_ORDER[0]].get("pass"))
        majority = _majority_answer(row["candidates"], list(ALL_OPERATORS))
        if majority is not None and verdict_for(
                f"Answer: {majority}",
                {"known_answer": row["known_answer"],
                 "expression": row["expression"]}) == "pass":
            majority_pass += 1

    return {
        "schema_version": 1,
        "run_kind": "m32_structured_repair_evaluation",
        "task_category": manifest["task_category"],
        "n_rows_evaluated": n,
        "frozen_trigger": {
            "source": "M30 full_telemetry (refit-verified)",
            "threshold_fail_probability": threshold,
            "trigger_count": len(triggered),
            "trigger_rate": len(triggered) / n,
            "trigger_precision": (trigger_hits / len(triggered)
                                  if triggered else None),
            "trigger_recall": (trigger_hits / len(fails) if fails else None),
            "false_alarm_count": len(triggered) - trigger_hits,
        },
        "original_label_distribution": {
            "pass": n - len(fails), "fail": len(fails)},
        "policy_metrics": metrics,
        "h1_repair_operator": {
            "population": "triggered_and_original_fail",
            "n": len(h1_rows),
            "bundle_rescue_rate": (sum(h1_bundle) / len(h1_rows)
                                   if h1_rows else None),
            "resample_rescue_rate": (sum(h1_resample) / len(h1_rows)
                                     if h1_rows else None),
            "delta_rescue_rate": ((sum(h1_bundle) - sum(h1_resample))
                                  / len(h1_rows) if h1_rows else None),
            "delta_bootstrap_95pct": h1_interval,
            "verdict": h1_verdict,
        },
        "h2_policy_usefulness": {
            "comparisons": h2,
            "verdict": h2_verdict,
        },
        "operator_candidate_stats": operator_stats,
        "operator_unique_rescues": union_others,
        "selectors_on_failing_originals": {
            "n_with_full_candidate_telemetry": len(rerank_rows),
            "verifier_first_pass_rate": (
                sum(1 for row in rerank_rows if any(
                    row["candidates"][name].get("pass")
                    for name in STRUCTURED_ORDER)) / len(rerank_rows)
                if rerank_rows else None),
            "telemetry_rerank_pick_passes_given_any_passing": (
                rerank_correct / rerank_when_possible
                if rerank_when_possible else None),
            "first_candidate_pass_rate": (
                first_candidate_pass / len(rerank_rows)
                if rerank_rows else None),
            "majority_pass_rate": (majority_pass / len(rerank_rows)
                                   if rerank_rows else None),
            "distribution_shift_caveat": (
                "candidate telemetry comes from repair prompts outside the "
                "M30 training prompt family; rerank results are exploratory"),
        },
        "tool_upper_bound_success_rate":
            metrics["telemetry_trigger_tool_upper_bound"][
                "verified_success_rate"],
        "tool_upper_bound_excluded_from_h1_h2": True,
        "claim_rules": manifest["hypotheses"],
        "per_task_predictions_persisted_publicly": False,
        "candidate_only": True,
        "production_gated": True,
        "privacy_check_status": "aggregate metrics/intervals only; no ids/text/paths",
    }


